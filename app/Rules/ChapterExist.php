<?php

namespace App\Rules;

use App\PhysicsSlot;
use Illuminate\Contracts\Validation\Rule;

class ChapterExist implements Rule
{
    /**
     * Create a new rule instance.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Determine if the validation rule passes.
     *
     * @param  string  $attribute
     * @param  mixed  $value
     * @return bool
     */
    public function passes($attribute, $value)
    {
        foreach ($value as  $chapterKey) {
            if (!PhysicsSlot::getChapters()->has($chapterKey)) {
                return false;
            }
        }
        return true;
    }

    /**
     * Get the validation error message.
     *
     * @return string
     */
    public function message()
    {
        return trans('validation.chapterexist');
    }
}
